    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Se castean los datos en cada campo de texto
       String password = txtContra.getText();
       int idCuenta = Integer.parseInt(txtCuentaOrigen.getText());
       int destino = Integer.parseInt(txtNumeroDestino.getText());
        double monto = Double.parseDouble(txtMontoTransferir.getText());
        //Se instancia la clase consignacion del modelo
         ClsConsignacion consig = new ClsConsignacion(monto, password, idCuenta, destino);
         //si todo se encuentra correcto, guarda con éxito
        if (controladorConsi.guardar(consig)) {
            JOptionPane.showMessageDialog(null, "Consignación registrada con éxito");
            //Carga en la tabla
            cargarTabla();
            //limpia las cajas de texto
            limpiarCampos();
        } else {
            //de lo contrario, ocurrió un error al guardar
            JOptionPane.showMessageDialog(null, "Hubo un error al realizar la consignación");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

