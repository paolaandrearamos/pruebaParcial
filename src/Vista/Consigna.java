    //Método para cargar los campos en la tabla de consignación
    private void cargarTabla() {
        clsConsignacion = controladorConsi.listar();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for (ClsConsignacion per : clsConsignacion) {
            model.addRow(new Object[]{
                per.getCuenta_origen(),
                per.getMonto(),
                per.getPassword(),
            per.getCuenta_destino()});
        }
    }
    
    //Método para limpiar los campos cada vez que sea requerido
    private void limpiarCampos() {
        txtContra.setText("");
        txtCuentaOrigen.setText("");
        txtMontoTransferir.setText("");
        txtNumeroDestino.setText("");
    }
